<?php

namespace App\Traits;

trait FileUpload
{
    public function fileName($str)
    {
        $str = trim(mb_strtolower($str));
        $str = preg_replace('/(à|á|ạ|ả|ã|â|ầ|ấ|ậ|ẩ|ẫ|ă|ằ|ắ|ặ|ẳ|ẵ)/', 'a', $str);
        $str = preg_replace('/(è|é|ẹ|ẻ|ẽ|ê|ề|ế|ệ|ể|ễ)/', 'e', $str);
        $str = preg_replace('/(ì|í|ị|ỉ|ĩ)/', 'i', $str);
        $str = preg_replace('/(ò|ó|ọ|ỏ|õ|ô|ồ|ố|ộ|ổ|ỗ|ơ|ờ|ớ|ợ|ở|ỡ)/', 'o', $str);
        $str = preg_replace('/(ù|ú|ụ|ủ|ũ|ư|ừ|ứ|ự|ử|ữ)/', 'u', $str);
        $str = preg_replace('/(ỳ|ý|ỵ|ỷ|ỹ)/', 'y', $str);
        $str = preg_replace('/(đ)/', 'd', $str);
        $str = preg_replace('/(_| )/', '-', $str);
        return $str;
    }

    public function uploadFile($folder, $file, $oldFile = null)
    {
        //dd($file->getClientOriginalName());
        $name = $this->fileName($file->getClientOriginalName());
        $fileName = 'audio-'.time().'-'.$name;
        $file->move($folder, $fileName);
        if ($oldFile != null && file_exists($folder . '/' . $oldFile)) {
            unlink($folder . '/' . $oldFile);
        }
        return $fileName;
    }
}
